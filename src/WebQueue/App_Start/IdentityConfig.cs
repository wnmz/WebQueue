using System.Net.Mail;
using System.Security.Claims;
using System.Threading.Tasks;
using WebQueue.Models;

namespace WebQueue
{
    public class EmailService : IIdentityMessageService
    {
        public async Task SendAsync(IdentityMessage emailParams)
        {
            var email = ConfigurationManager.AppSettings["Email"];
            var pass = ConfigurationManager.AppSettings["Email_Password"];
            var smtpServer = ConfigurationManager.AppSettings["Email_Smtp"];
            var smtpPort = int.Parse(ConfigurationManager.AppSettings["Email_Smtp_Port"]);

            var from = new MailAddress(email, "Server");
            var to = new MailAddress(emailParams.Destination);
            var message = new MailMessage(from, to)
            {
                IsBodyHtml = true,
                Subject = emailParams.Subject,
                Body = emailParams.Body
            };
            var smtp = new SmtpClient(smtpServer, smtpPort)
            {
                EnableSsl = true,
                DeliveryMethod = System.Net.Mail.SmtpDeliveryMethod.Network,
                UseDefaultCredentials = false,
                Credentials = new NetworkCredential(email, pass)
            };
            await smtp.SendMailAsync(message);
            Console.WriteLine("Письмо отправлено");
        }
    }

